class loops{
    public static void main(String[] args) {
        //print pattern
        int k=4;
        for(int i=0;i<=k;i++){
            for(int j=0;j<=i;j++){
           System.out.print("*");

            }
           System.out.print("\n");

            }
            //reverse
            int k1=4;
        for(int i=k1;i>0;i--){
            for(int j=0;j<i;j++){
           System.out.print("*");

            }
           System.out.print("\n");

            }
            //a
        }
}
